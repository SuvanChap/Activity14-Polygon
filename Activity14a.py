#Write a program to draw a star using a turtle?
import turtle
turtle.Screen().bgcolor("red")
turtle.Screen().setup(500,500)
polygon = turtle.Turtle()

polygon.forward(100)
polygon.left(120)
polygon.forward(100)
polygon.left(120)
polygon.forward(100)
polygon.penup()
polygon.right(150)
polygon.forward(50)
polygon.right(90)
polygon.pendown()
polygon.forward(100)
polygon.right(120)
polygon.forward(100)
polygon.right(120)
polygon.forward(100)




turtle.done()
